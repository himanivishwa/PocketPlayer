void setup() {
  pinMode(BUTTON_PIN, INPUT_PULLUP); // pull up so when it connects to gnd it changes in code
  u8g2.begin();
  Serial.begin(115200);
}

// Helper function to format time from seconds to "MM:SS"
void formatTime(int seconds, char* buffer) {
  int minutes = seconds / 60;
  int secs = seconds % 60;
  sprintf(buffer, "%d:%02d", minutes, secs);
}

void drawGUI() {
  u8g2.clearBuffer();

  // Song Title - scroll within 64px width
  u8g2.setFont(u8g2_font_6x10_tr);
  const char* songTitle = "Lovers'Rock - TV Girl";
  textWidth = u8g2.getStrWidth(songTitle);
  
  // Set clip window to 64px width
  u8g2.setClipWindow(0, 0, 64, 64);
  u8g2.drawStr(scrollX, 10, songTitle);
  u8g2.setMaxClipWindow();  // Reset to full screen

  // Artist
  // u8g2.setFont(u8g2_font_6x10_tr);
  // u8g2.drawStr(0, 22, "Artist Name");

  // cat image
  u8g2.drawXBMP(0,21, 128, 47, catbushBitmp);

  // Play/Pause Icon
  int buttonState = digitalRead(BUTTON_PIN);

  if (buttonState == LOW && lastButtonState == HIGH && (millis() - lastButtonPressTime > 200)){
    toggleState = !toggleState;
    lastButtonPressTime = millis();
  }

  lastButtonState = buttonState;

  if (toggleState == 0) {
    //u8g2.drawStr(0, 60, ">");   // simple play symbol
    u8g2.drawXBMP(115,5, 12, 12, playBitmp);
  } 
  if (toggleState == 1){
    //u8g2.drawStr(0, 60, "||");  // pause
    u8g2.drawXBMP(115,5, 12, 12, pauseBitmp);
  }

  // Progress Bar
  progress = (currentTime * 100) / totalDuration; // Update progress based on current time and total duration

  int barWidth = 60;
  int filled = (progress * barWidth) / 100;

  u8g2.drawFrame(0, 12, barWidth, 4);      // outer box
  u8g2.drawBox(0, 12, filled, 4);          // filled part

  // Timeline display (current time / total duration)
  char currentTimeStr[10];
  char totalTimeStr[10];
  formatTime(currentTime, currentTimeStr);
  formatTime(totalDuration, totalTimeStr);
  
  char timelineStr[30];
  sprintf(timelineStr, "%s/%s", currentTimeStr, totalTimeStr);
  u8g2.setFont(u8g2_font_5x7_tr );   // Reset to default
  u8g2.drawStr(64, 16, timelineStr);

  // Volume
  char volStr[10];
  sprintf(volStr, "V:%d", volume);
  u8g2.drawStr(105, 60, volStr);

  u8g2.sendBuffer();
}

void loop() {
  drawGUI();

  // Scroll the text within 64px area
  scrollX--;
  if (scrollX < -textWidth) {
    scrollX = 64;  // Reset to right edge of 64px area
  }

  // demo animation
  if (toggleState == 0) { // If paused, reset time to 0
    currentTime = currentTime;
  }
  else if (toggleState == 1) { // Update time every 600ms when playing
    currentTime = (currentTime + 1) % totalDuration;  // Increment time in demo
  }
  
}